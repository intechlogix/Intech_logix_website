// Breadcrumb generators
export const generateBreadcrumbs = (path) => {
  const pathSegments = path.split('/').filter(Boolean);
  const breadcrumbs = [{ name: "Home", href: "/" }];
  
  let currentPath = '';
  pathSegments.forEach((segment, index) => {
    currentPath += `/${segment}`;
    
    // Convert segment to readable name
    let name = segment.charAt(0).toUpperCase() + segment.slice(1);
    name = name.replace('-', ' ');
    
    // Special cases
    if (segment === 'services') name = 'Services';
    if (segment === 'digital-marketing') name = 'Digital Marketing';
    if (segment === 'web-design') name = 'Web Design';
    if (segment === 'app-development') name = 'App Development';
    if (segment === 'video-editing') name = 'Video Editing';
    
    breadcrumbs.push({
      name,
      href: currentPath
    });
  });
  
  return breadcrumbs;
};

